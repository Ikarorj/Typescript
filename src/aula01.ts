// Exemplo de declaração de variáveis em TypeScript

//variaveis são usadas para armazenar dados que podem ser alterados durante a execução do programa
// Neste exemplo, vamos declarar algumas variáveis e imprimir seus valores no console
 
// Variáveis podem ser declaradas com 'let', 'const' ou 'var'
let nome = 'bruno';
let idade = 30;
// 'let' permite que a variável seja reatribuída

// 'const' cria uma constante que não pode ser reatribuída
// 'var' é uma forma antiga de declarar variáveis, menos recomendada atualmente

//console.log imprime o valor das variáveis no console
// Aqui, estamos imprimindo o nome e a idade
console.log(nome);
console.log(idade);

