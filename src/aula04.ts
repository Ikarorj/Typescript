// inferência de tipos em TypeScript
// O TypeScript é uma linguagem de programação que 
// adiciona tipagem estática ao JavaScript, permitindo que os desenvolvedores especifiquem tipos de dados para variáveis, 
// parâmetros e retornos de funções. Isso ajuda a evitar erros comuns em tempo de execução e melhora a legibilidade do código.
let nome1: string;
nome1 = 'Bruno';
// Exemplo de declaração de variáveis em TypeScript
// A variável nome1 é declarada como uma string 

// ´varaivel valor1´ é declarada como um número
// O tipo de dado é inferido automaticamente pelo TypeScript
//int, float, bin, hex, octal são tipos de dados numéricos
// No TypeScript, não é necessário declarar o tipo de dado explicitamente, ele é inferido pelo valor atribuído
let valor1 = 1000; 
// A variável valor1 é declarada como um número
// O valor 1000 é atribuído a ela, e o TypeScript entende que é um número
console.log(valor1);

